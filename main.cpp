#include <iostream> // Программа учебная

using namespace std;

int main() {
    int num1, num2;
    cout << "Введите первое целое число: ";
    cin >> num1;
    cout << "Введите второе целое число: ";
    cin >> num2;
    cout << "Сумма чисел: " << num1 + num2 << endl;
    cout << "Разность чисел: " << num1 - num2 << endl;
    return 0;
}
